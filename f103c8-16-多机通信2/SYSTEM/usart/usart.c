	//入口参数:point:数组名  ,   Length:数组长度
//出口参数 

	//////////////////////////////  0x00 和 \0
void MY_Send_Data(USART_TypeDef* USARTx, char* point, u8 Length)
{
	USART_ClearFlag(USARTx,USART_FLAG_TC);
	while(Length--)
	{
		USART_SendData(USARTx,*point++);
		while(USART_GetFlagStatus(USARTx,USART_FLAG_TC)!=SET);
	}
}









