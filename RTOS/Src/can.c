void CAN_Filter_Config(CAN_HandleTypeDef *hcan,uint32_t FilterBank)
{
    CAN_FilterTypeDef  sFilterConfig;
    
    sFilterConfig.FilterBank = FilterBank;                       //CAN过滤器编号，范围0-27
    sFilterConfig.FilterMode = CAN_FILTERMODE_IDMASK;   //CAN过滤器模式，掩码模式或列表模式
    sFilterConfig.FilterScale = CAN_FILTERSCALE_32BIT;  //CAN过滤器尺度，16位或32位
    sFilterConfig.FilterIdHigh = 0x000;			//32位下，存储要过滤ID的高16位
    sFilterConfig.FilterIdLow = 0x0000;					//32位下，存储要过滤ID的低16位
    sFilterConfig.FilterMaskIdHigh = 0x0000;			//掩码模式下，存储的是掩码
    sFilterConfig.FilterMaskIdLow = 0x0000;
    sFilterConfig.FilterFIFOAssignment = 0;				//报文通过过滤器的匹配后，存储到哪个FIFO
    sFilterConfig.FilterActivation = ENABLE;    		//激活过滤器
    sFilterConfig.SlaveStartFilterBank = 0;
    
    if (HAL_CAN_ConfigFilter(hcan, &sFilterConfig) != HAL_OK)
		{
//			printf("CAN Filter Config Fail!\r\n");
			Error_Handler();
    }
//    printf("CAN Filter Config Success!\r\n");
}


u8 CAN_Send_Msg(CAN_HandleTypeDef *hcan,u8* msg,uint32_t StdId,u8 len)
{
	CAN_TxHeaderTypeDef TxMessage;
	u8 i;
	u32 TxMailbox;
	u8 message[8];
	TxMessage.StdId=StdId;        //标准标识符  0x200
	TxMessage.ExtId=0x12;        //扩展标识符(29位)
	TxMessage.IDE=CAN_ID_STD;    //使用标准帧
	TxMessage.RTR=CAN_RTR_DATA;  //数据帧
	TxMessage.DLC=len;                
	for(i=0;i<len;i++)
	{
		message[i]=msg[i];
	}
	if(HAL_CAN_AddTxMessage(hcan, &TxMessage, message, &TxMailbox) != HAL_OK)//发送
	{
		return 1;
	}
	while(HAL_CAN_GetTxMailboxesFreeLevel(hcan) != 3) {}
	return 0;
}




